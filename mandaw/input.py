import pygame

class Input:
    def __init__(self):
        self.UP = pygame.K_UP
        self.DOWN = pygame.K_DOWN
        self.LEFT = pygame.K_LEFT
        self.RIGHT = pygame.K_RIGHT
        
        self.W = pygame.K_w
        self.A = pygame.K_a
        self.S = pygame.K_s
        self.D = pygame.K_d

        self.SPACE = pygame.K_SPACE